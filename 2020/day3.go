func numTrees(grid []string, right int, down int) int {
	x := 0
	y := 0

	path := ""

	for y < len(grid){
		line := grid[y]
		if line == ""{
			break
		}

		path += string(line[x])
		x += right
		y += down

		x %= len(line)


	}
	
	return strings.Count(path, "#")
}

